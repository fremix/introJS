// Diferencia entre metodo y funcion 

const numero1 = 20;
const numero2 = '20';

console.log( parseInt(numero2) );  // parseInt() es una Funcion 
console.log( numero1.toString() ); // toString() es un metodo
/* Los que tiene un punto seguido del nombre de la funcion son metodos
*  LOs que tiene el nombre de la funcion seguido del parentesos son Funciones
*/
